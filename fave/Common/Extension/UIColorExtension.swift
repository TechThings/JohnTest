import Foundation

extension UIColor {

    final class func faveBackground() -> UIColor {
        return UIColor(hexStringFast:"#f1f2f3")
    }

    final class func favePink() -> UIColor {
        return UIColor(hexStringFast:"#de206a")
    }

    final class func faveGreen() -> UIColor {
        return UIColor(hexStringFast:"#48b049")
    }

    final class func faveDarkGray() -> UIColor {
        return UIColor(hexStringFast:"#99a9b4")
    }
    final class func faveBlue() -> UIColor {
        return UIColor(hexStringFast:"#33a8d1")
    }

    final class func faveRed() -> UIColor {
        return UIColor(hexStringFast:"#FF0000")
    }

    final class func faveErrorRed() -> UIColor {
        return UIColor(hexStringFast:"#FFEDF4")
    }

    final class func faveRatingGreen() -> UIColor {
        return UIColor(hexStringFast:"#6bb617")
    }

    final class func faveRatingSubGreen() -> UIColor {
        return UIColor(hexStringFast:"#879e09")
    }

    final class func faveRatingBrown() -> UIColor {
        return UIColor(hexStringFast:"#b18d11")
    }

    final class func faveWarmGray() -> UIColor {
        return UIColor(hexStringFast: "#979797")
    }

    final class func faveRatingSubOrange() -> UIColor {
        return UIColor(hexStringFast:"#cf8016")
    }

    final class func faveRatingOrange() -> UIColor {
        return UIColor(hexStringFast:"#e8771b")
    }
}
